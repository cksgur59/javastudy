package kr.co.ezen;

public class Dudu {

	
	private int ggg;
	private int hhh;
	private int jjj;
	private int ooo;
	public Dudu(int ggg, int hhh, int jjj, int ooo) {
		super();
		this.ggg = ggg;
		this.hhh = hhh;
		this.jjj = jjj;
		this.ooo = ooo;
	}
	public int getGgg() {
		return ggg;
	}
	public void setGgg(int ggg) {
		this.ggg = ggg;
	}
	public int getHhh() {
		return hhh;
	}
	public void setHhh(int hhh) {
		this.hhh = hhh;
	}
	public int getJjj() {
		return jjj;
	}
	public void setJjj(int jjj) {
		this.jjj = jjj;
	}
	public int getOoo() {
		return ooo;
	}
	public void setOoo(int ooo) {
		this.ooo = ooo;
	}
	
	
}
