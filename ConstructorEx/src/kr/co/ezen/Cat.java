package kr.co.ezen;

public class Cat {

	
	private int qqq;
	private int www;
	private int eee;
	private int zxc;
	private Mouse mse;
	
	public Cat() {
		
	}

	public Cat(int qqq, int www, int eee, int zxc, Mouse mse) {
		super();
		this.qqq = qqq;
		this.www = www;
		this.eee = eee;
		this.zxc = zxc;
		this.mse = mse;
	}

	public int getQqq() {
		return qqq;
	}

	public void setQqq(int qqq) {
		this.qqq = qqq;
	}

	public int getWww() {
		return www;
	}

	public void setWww(int www) {
		this.www = www;
	}

	public int getEee() {
		return eee;
	}

	public void setEee(int eee) {
		this.eee = eee;
	}

	public int getZxc() {
		return zxc;
	}

	public void setZxc(int zxc) {
		this.zxc = zxc;
	}

	public Mouse getMse() {
		return mse;
	}

	public void setMse(Mouse mse) {
		this.mse = mse;
	}
	
	
}
