package kr.co.ezen;

public class Dongki {

	
	private int rrr;
	private int ttt;
	private int yyy;
	private int uuu;
	private Dudu dudu;
	public Dongki(int rrr, int ttt, int yyy, int uuu, Dudu dudu) {
		super();
		this.rrr = rrr;
		this.ttt = ttt;
		this.yyy = yyy;
		this.uuu = uuu;
		this.dudu = dudu;
	}
	public int getRrr() {
		return rrr;
	}
	public void setRrr(int rrr) {
		this.rrr = rrr;
	}
	public int getTtt() {
		return ttt;
	}
	public void setTtt(int ttt) {
		this.ttt = ttt;
	}
	public int getYyy() {
		return yyy;
	}
	public void setYyy(int yyy) {
		this.yyy = yyy;
	}
	public int getUuu() {
		return uuu;
	}
	public void setUuu(int uuu) {
		this.uuu = uuu;
	}
	public Dudu getDudu() {
		return dudu;
	}
	public void setDudu(Dudu dudu) {
		this.dudu = dudu;
	}
	
	
}
